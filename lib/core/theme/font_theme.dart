import 'package:flutter/material.dart';

class FontTheme {
  // Black
  static TextStyle blackBold20() {
    return const TextStyle(
      color: Colors.black,
      fontSize: 20,
    );
  }

  // White
  static TextStyle whiteBold20() {
    return const TextStyle(
      color: Colors.white,
      fontSize: 20,
    );
  }
}
